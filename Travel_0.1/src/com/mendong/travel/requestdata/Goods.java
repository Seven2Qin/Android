package com.mendong.travel.requestdata;

import java.util.ArrayList;
import java.util.List;

public class Goods {

	private String name;//商品名称
	private String info;//商品介绍
	private List picNameList = new ArrayList<String>();//商品图片名字
	private float  price;//商品价格
	private String param;//商品参数
	private String address;//商品产地

    public String getName() {  
        return name;  
    }  
  
    public void setName(String name) {  
        this.name = name;  
    }  
    public String getInfo() {  
    	return info;  
    }  
    
    public void setInfo(String info) {  
    	this.info = info;  
    }  
    public List getPicNameList() {  
    	return picNameList;  
    }  
    
    public void setPicNameList(String picName) {  
    	this.picNameList.add(picName);  
    }  
    public float getPrice() {  
    	return price;  
    }  
    
    public void setPrice(float price) {  
    	this.price = price;  
    }  
    public String getParam() {  
    	return param;  
    }  
    
    public void setParam(String param) {  
    	this.param = param;  
    }  
    public String getAddress() {  
    	return address;  
    }  
    
    public void setAddress(String address) {  
    	this.address = address;  
    }  
  
   

}
