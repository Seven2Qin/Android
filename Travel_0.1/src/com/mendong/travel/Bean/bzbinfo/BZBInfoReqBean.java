/**
 * 类名称：     BZBInfoReqBean
 * 作者：         Administrator
 * 创建时间：  2013年7月22日 下午2:12:57
 * 类描述：    
 * 版权声明 ： Copyright (C) 2008-2010 RichPeak
 * 修改时间：  2013年7月22日 下午2:12:57
 *
 */
public final class BZBInfoReqBean
{
    /**
     * 请求类型
     */
    private String opType;
    
    /**
     * 商家编号
     */
    private String id;

    /**
     * 方法名称：  getOpType
     * 作者：          Administrator
     * 方法描述：  
     * 字段类型：  String
     * 返回字段：  opType
     * 备注：
     */
    public String getOpType()
    {
        return opType;
    }

    /**
     * 方法名称： setOpType
     * 作者：         Administrator
     * 方法描述：  
     * 字段类型：  String
     * 设置字段：  设置 opType 给 opType
     * 备注：
     */
    public void setOpType(String opType)
    {
        this.opType = opType;
    }

    /**
     * 方法名称：  getId
     * 作者：          Administrator
     * 方法描述：  
     * 字段类型：  String
     * 返回字段：  id
     * 备注：
     */
    public String getId()
    {
        return id;
    }

    /**
     * 方法名称： setId
     * 作者：         Administrator
     * 方法描述：  
     * 字段类型：  String
     * 设置字段：  设置 id 给 id
     * 备注：
     */
    public void setId(String id)
    {
        this.id = id;
    }
    
    /* (non-Javadoc)
     * (覆盖方法)
     * 方法名称：  toString
     * 作者：            Administrator
     * 方法描述：    
     * @see java.lang.Object#toString()
     */
    @Override
    public String toString()
    {
        StringBuffer sb = new StringBuffer();

        if ((null != opType))
        {
            sb.append("opType=").append(opType).append(',');
        }
        if ((null != id))
        {
            sb.append("id=").append(id).append(',');
        }
        return sb.toString().substring(0, sb.toString().length());
    }
}
